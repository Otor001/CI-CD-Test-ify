package Tasks;

public class Task9A {
    public static void main(String[] args) {
        // Loop through numbers 1 to 10
        for (int i = 1; i <= 10; i++) {
            // Check if the number is even or odd
            if (i % 2 == 0) {
                System.out.println(i + " is Even");
            } else {
                System.out.println(i + " is Odd");
            }
        }
    }
}
